async function seedStates() {
  try {
    await mongoose.connect(process.env.MONGODB_URI || "mongodb://localhost:27017/smokefree-india", {
      useNewUrlParser: true,
      useUnifiedTopology: true,
    })

    console.log("üóëÔ∏è  Clearing existing states...")
    await State.deleteMany({})

    console.log("üå± Seeding states data...")
    await State.insertMany(statesData)

    console.log(`‚úÖ Successfully seeded ${statesData.length} states!`)
    process.exit(0)
  } catch (error) {
    console.error("‚ùå Error seeding states:", error)
    process.exit(1)
  }
}

seedStates()
